#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	k;

	if (!s)
		return ;
	k = 0;
	while (s[k])
	{
		(*f)(k, &s[k]);
		k++;
	}
}
